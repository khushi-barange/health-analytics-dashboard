import pandas as pd
import numpy as np
import sqlite3
import json
from datetime import datetime

df = pd.read_csv('data/health_data.csv')
conn = sqlite3.connect('data/health_analytics.db')

avg_health = df['health_score'].mean()
at_risk = (df['health_score'] < 40).sum()
at_risk_pct = at_risk / len(df) * 100
top_factor = df[['sleep_hours','steps_per_day','stress_level',
                  'exercise_hours_week']].corrwith(df['health_score']).abs().idxmax()

good_sleep = df[df['sleep_hours'] >= 7]['health_score'].mean()
poor_sleep = df[df['sleep_hours'] < 7]['health_score'].mean()
sleep_diff = good_sleep - poor_sleep

smoker_score = df[df['smoking'] == 1]['health_score'].mean()
nonsmoker_score = df[df['smoking'] == 0]['health_score'].mean()
smoking_diff = nonsmoker_score - smoker_score

high_stress = df[df['stress_level'] >= 7]['health_score'].mean()
low_stress = df[df['stress_level'] <= 3]['health_score'].mean()
stress_diff = low_stress - high_stress

high_steps = df[df['steps_per_day'] >= 10000]['health_score'].mean()
low_steps = df[df['steps_per_day'] < 5000]['health_score'].mean()
steps_diff = high_steps - low_steps

diet_scores = df.groupby('diet_type')['health_score'].mean().sort_values(ascending=False)
best_diet = diet_scores.index[0]
worst_diet = diet_scores.index[-1]
diet_diff = diet_scores.iloc[0] - diet_scores.iloc[-1]

kpis = {
    'avg_health_score': round(avg_health, 1),
    'at_risk_count': int(at_risk),
    'at_risk_pct': round(at_risk_pct, 1),
    'top_health_factor': top_factor,
    'good_sleep_score': round(good_sleep, 1),
    'poor_sleep_score': round(poor_sleep, 1),
    'sleep_impact': round(sleep_diff, 1),
    'smoker_score': round(smoker_score, 1),
    'nonsmoker_score': round(nonsmoker_score, 1),
    'smoking_impact': round(smoking_diff, 1),
    'high_stress_score': round(high_stress, 1),
    'low_stress_score': round(low_stress, 1),
    'stress_impact': round(stress_diff, 1),
    'high_steps_score': round(high_steps, 1),
    'low_steps_score': round(low_steps, 1),
    'steps_impact': round(steps_diff, 1),
    'best_diet': best_diet,
    'worst_diet': worst_diet,
    'diet_impact': round(diet_diff, 1)
}

