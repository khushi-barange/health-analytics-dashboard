def get_health_recommendations(age, sleep_hours, stress_level,
                                steps_per_day, smoking, bmi, diet_type):
    recommendations = []
    risk_score = 0

    if sleep_hours < 6:
        recommendations.append("URGENT: You are severely sleep deprived. Target 7-8 hours. Sleep deprivation increases disease risk by 40%.")
        risk_score += 3
    elif sleep_hours < 7:
        recommendations.append("Sleep: Increase sleep to 7+ hours. Even 30 extra minutes improves cognitive function and immunity.")
        risk_score += 1

    if steps_per_day < 5000:
        recommendations.append("URGENT: Very low activity level. Start with a 20-minute daily walk. Sedentary lifestyle doubles heart disease risk.")
        risk_score += 3
    elif steps_per_day < 8000:
        recommendations.append("Activity: Aim for 8,000-10,000 steps daily. Try parking farther or taking stairs.")
        risk_score += 1

    if stress_level >= 8:
        recommendations.append("URGENT: Critically high stress. Consider meditation, therapy, or workload reduction immediately.")
        risk_score += 3
    elif stress_level >= 6:
        recommendations.append("Stress: Practice 10 minutes of daily meditation or deep breathing exercises.")
        risk_score += 1

    if smoking == 1:
        recommendations.append("URGENT: Smoking is your single biggest health risk. Quitting adds 10+ years to life expectancy.")
        risk_score += 4

    if bmi > 30:
        recommendations.append("BMI: Your BMI indicates obesity. A 5-10% weight reduction significantly lowers disease risk.")
        risk_score += 2
    elif bmi > 25:
        recommendations.append("BMI: Slightly above healthy range. Combine 30 min daily exercise with balanced diet.")
        risk_score += 1

    if risk_score == 0:
        recommendations.append("Excellent! Your lifestyle indicators are all in healthy ranges. Maintain your current habits.")
        recommendations.append("Consider adding strength training 2x per week to further boost your health score.")

    if risk_score <= 2:
        risk_level = "LOW RISK"
    elif risk_score <= 5:
        risk_level = "MODERATE RISK"
    else:
        risk_level = "HIGH RISK"

    return recommendations, risk_level

